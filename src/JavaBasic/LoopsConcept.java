package JavaBasic;

public class LoopsConcept {

	public static void main(String[] args) {
		// 1 while loop
		
				int i =1; // initialization
				while(i<=10) //conditionl 
					{
					System.out.println(i);
					// i = i+1; or below here not give increment it is give inifity loop
					i++;	// incremental decrimental loop
				}
				System.out.println("**************");
				
				// 2 for loop 
			    // j++ or j=j+1
				for(int j=1; j<=10;j++)
				{
					System.out.println(j);
				}
				
				// print 10 to 1
				// k means k=k-1
				for(int k=10; k>=-10; k-- ) {
					System.out.println(k);
				}
				

			

	}

}
